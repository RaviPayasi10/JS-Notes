
// HTTPS - It is a protocol through which we can communicate with servers
// in a network

// It is a request which we send to server and in return it gives us a response.

// HTTP methods - 
// 1. Get - to retreive data
// 2. Post - to create data
// 3. Put - to update data
// 4. Delete - to delete data

// HTTP status code - 
// Info related to requests.
// 100-199 => Information message
// 200 - Success
// 300 - Redirect
// 400 - Client side
// 404 - Error
// 500 - Server side

// All requests and response are objects and have 2 parts - A header and a Body
// Actual data is stored in body
// Meta data about data is stored in header.

